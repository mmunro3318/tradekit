"""Rules catalog v1 — R-001..R-016 (DESIGN §7.2; CTO addendum, story-3
pins). REAL this batch (declarative data the tests read, like `_dials.py` —
CTO's batch-C red/green split call): every `Rule.check` below is a PURE
function of `(action, ctx)`, unit-testable with a synthetic
`_context.PolicyContext` and no `_evaluate.py`/ledger involvement at all —
that pure core stays a stub this batch, but the rules it will eventually run
do not need it to be unit-tested individually.

IDs are stable forever (§7.2). `rules/RULES.md` (generated by
`tk policy status --rules`, batch-D+ CLI work) is produced FROM this
registry — one source of truth, never hand-edited.

Anti-permissive convention (CTO addendum): a rule whose needed context
field is `None` denies with `"insufficient_context"` folded into the
`RuleHit.measured` string (`"insufficient_context:<field>"`), NEVER a
silent pass. Fields that are legitimately empty in P2 (no open positions,
nothing graded yet) pass vacuously instead — see `tests/ASSUMPTIONS.md`'s
batch-C entry enumerating the split, rule by rule.
"""

from __future__ import annotations

from collections.abc import Callable
from dataclasses import dataclass
from decimal import Decimal

from tradekit.contracts import ProposedAction, RuleHit
from tradekit.policy._context import PolicyContext


@dataclass(frozen=True)
class Rule:
    id: str
    applies_to: frozenset[str]
    check: Callable[[ProposedAction, PolicyContext], RuleHit]
    why: str


def _hit(rule_id: str, *, allow: bool, measured: str | None, limit: str | None) -> RuleHit:
    return RuleHit(
        rule_id=rule_id,
        outcome="pass" if allow else "fail",
        measured=measured,
        limit=limit,
    )


def _insufficient(rule_id: str, field: str, limit: str | None = None) -> RuleHit:
    return _hit(rule_id, allow=False, measured=f"insufficient_context:{field}", limit=limit)


def _order_notional(action: ProposedAction) -> Decimal | None:
    """qty * limit_price of the proposed order — `None` when the action
    carries no order, or a market order with no limit price to price it
    against (insufficient_context for any rule that needs a notional)."""
    order = action.order
    if order is None or order.limit_price is None:
        return None
    return order.qty * order.limit_price


def _is_live(action: ProposedAction) -> bool:
    return action.account_ref.startswith("live:")


def _is_paper(action: ProposedAction) -> bool:
    return action.account_ref.startswith("paper:")


def _is_advisory(action: ProposedAction) -> bool:
    return action.account_ref.startswith("advisory:")


# ---------------------------------------------------------------------------
# R-001 — kill switch
# ---------------------------------------------------------------------------


def _check_r001(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if ctx.halted:
        return _hit(
            "R-001", allow=False, measured="halted", limit=ctx.halt_reason or "unresolved"
        )
    return _hit("R-001", allow=True, measured="not_halted", limit=None)


# ---------------------------------------------------------------------------
# R-002 — promotion tier permits this account_ref + action
# ---------------------------------------------------------------------------

_MIN_TIER_RANK = {"T0": 0, "T1": 1, "T2": 2}


def _required_tier(action: ProposedAction) -> str:
    if _is_live(action):
        return "T2"
    if _is_paper(action):
        return "T1"
    return "T0"  # advisory/research


def _check_r002(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if ctx.account_tier is None:
        return _insufficient("R-002", "account_tier")
    required = _required_tier(action)
    ok = _MIN_TIER_RANK[ctx.account_tier] >= _MIN_TIER_RANK[required]
    return _hit("R-002", allow=ok, measured=ctx.account_tier, limit=required)


# ---------------------------------------------------------------------------
# R-003 — sufficient settled balance incl. fees
# ---------------------------------------------------------------------------


def _check_r003(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    notional = _order_notional(action)
    if notional is None:
        return _insufficient("R-003", "order_notional")
    if ctx.settled_balance_usd is None:
        return _insufficient("R-003", "settled_balance_usd")
    ok = ctx.settled_balance_usd >= notional
    return _hit("R-003", allow=ok, measured=str(notional), limit=str(ctx.settled_balance_usd))


# ---------------------------------------------------------------------------
# R-004 — per-asset allowlist (live only, SME §5)
# ---------------------------------------------------------------------------


def _check_r004(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if not _is_live(action):
        return _hit("R-004", allow=True, measured="not_live", limit=None)
    if action.order is None:
        return _insufficient("R-004", "order")
    symbol = action.order.asset.symbol
    ok = symbol in ctx.dials.allowed_assets_live
    return _hit(
        "R-004", allow=ok, measured=symbol, limit=",".join(ctx.dials.allowed_assets_live)
    )


# ---------------------------------------------------------------------------
# R-005 — max position notional (live: $25; paper: 10% equity)
# ---------------------------------------------------------------------------


def _check_r005(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    notional = _order_notional(action)
    if notional is None:
        return _insufficient("R-005", "order_notional")
    if _is_paper(action):
        if ctx.account_equity_usd is None:
            return _insufficient("R-005", "account_equity_usd")
        limit = ctx.dials.max_position_pct_paper * ctx.account_equity_usd
    else:
        limit = ctx.dials.max_position_usd_live
    return _hit("R-005", allow=notional <= limit, measured=str(notional), limit=str(limit))


# ---------------------------------------------------------------------------
# R-006 — max total live exposure (D12)
# ---------------------------------------------------------------------------


def _check_r006(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    notional = _order_notional(action)
    if notional is None:
        return _insufficient("R-006", "order_notional")
    projected = ctx.live_exposure_usd + notional
    limit = ctx.dials.max_total_live_exposure_usd
    return _hit("R-006", allow=projected <= limit, measured=str(projected), limit=str(limit))


# ---------------------------------------------------------------------------
# R-007 — daily trade count (live 3/day, paper 20/day)
# ---------------------------------------------------------------------------


def _check_r007(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if ctx.trades_today_count is None:
        return _insufficient("R-007", "trades_today_count")
    limit = (
        ctx.dials.max_daily_trades_live if _is_live(action) else ctx.dials.max_daily_trades_paper
    )
    ok = ctx.trades_today_count < limit
    return _hit("R-007", allow=ok, measured=str(ctx.trades_today_count), limit=str(limit))


# ---------------------------------------------------------------------------
# R-008 — min notional $10
# ---------------------------------------------------------------------------


def _check_r008(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    notional = _order_notional(action)
    if notional is None:
        return _insufficient("R-008", "order_notional")
    limit = ctx.dials.min_notional_usd
    return _hit("R-008", allow=notional >= limit, measured=str(notional), limit=str(limit))


# ---------------------------------------------------------------------------
# R-009 — drawdown circuit breaker (30d peak >= 10% -> no new positions)
# ---------------------------------------------------------------------------


def _check_r009(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if ctx.trailing_30d_drawdown_pct is None:
        return _insufficient("R-009", "trailing_30d_drawdown_pct")
    limit = ctx.dials.drawdown_breaker_pct
    ok = ctx.trailing_30d_drawdown_pct < limit
    return _hit(
        "R-009", allow=ok, measured=str(ctx.trailing_30d_drawdown_pct), limit=str(limit)
    )


# ---------------------------------------------------------------------------
# R-010 — thesis prerequisites: approved review + numeric EV + snapshot
# ---------------------------------------------------------------------------


def _check_r010(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if ctx.thesis_review_artifact_id is None:
        return _insufficient("R-010", "thesis_review_artifact_id")
    if ctx.thesis_market_snapshot_id is None:
        return _insufficient("R-010", "thesis_market_snapshot_id")
    if ctx.thesis_ev_ok is None:
        return _insufficient("R-010", "thesis_ev_ok")
    return _hit("R-010", allow=bool(ctx.thesis_ev_ok), measured=str(ctx.thesis_ev_ok), limit="True")


# ---------------------------------------------------------------------------
# R-011 — live sequence budget: T2 grants max 3 live trades
# ---------------------------------------------------------------------------


def _check_r011(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if not _is_live(action):
        return _hit("R-011", allow=True, measured="not_live", limit=None)
    if ctx.live_trades_remaining is None:
        return _insufficient("R-011", "live_trades_remaining")
    ok = ctx.live_trades_remaining > 0
    return _hit(
        "R-011",
        allow=ok,
        measured=str(ctx.live_trades_remaining),
        limit=str(ctx.dials.live_sequence_budget),
    )


# ---------------------------------------------------------------------------
# R-012 — sizing purity (F6): submitted size vs recorded SizingComputed
# ---------------------------------------------------------------------------


def _check_r012(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    notional = _order_notional(action)
    if notional is None:
        return _insufficient("R-012", "order_notional")
    if ctx.recorded_sizing_usd is None:
        return _insufficient("R-012", "recorded_sizing_usd")
    recorded = ctx.recorded_sizing_usd
    if recorded == 0:
        return _insufficient("R-012", "recorded_sizing_usd")
    deviation = abs(notional - recorded) / recorded
    limit = ctx.dials.sizing_tolerance_pct
    return _hit("R-012", allow=deviation <= limit, measured=str(deviation), limit=str(limit))


# ---------------------------------------------------------------------------
# R-013 — correlation cap: |r| > 0.75 to an open position
# ---------------------------------------------------------------------------


def _check_r013(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if not ctx.open_position_correlations:
        return _hit("R-013", allow=True, measured="no_open_positions", limit=None)
    worst_symbol, worst_corr = max(
        ctx.open_position_correlations.items(), key=lambda kv: abs(kv[1])
    )
    limit = ctx.dials.correlation_cap
    ok = abs(worst_corr) <= limit
    return _hit(
        "R-013", allow=ok, measured=f"{worst_symbol}:{worst_corr}", limit=str(limit)
    )


# ---------------------------------------------------------------------------
# R-014 — advisory cooling-off: manual trades > $200 need thesis age >= 24h
# ---------------------------------------------------------------------------


def _check_r014(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if not _is_advisory(action):
        return _hit("R-014", allow=True, measured="not_advisory", limit=None)
    notional = _order_notional(action)
    if notional is None:
        return _insufficient("R-014", "order_notional")
    if notional <= ctx.dials.cooling_off_notional_usd:
        return _hit("R-014", allow=True, measured=str(notional), limit=None)
    if ctx.thesis_age_hours is None:
        return _insufficient("R-014", "thesis_age_hours")
    limit_hours = Decimal(ctx.dials.cooling_off_hours)
    ok = ctx.thesis_age_hours >= limit_hours
    return _hit("R-014", allow=ok, measured=str(ctx.thesis_age_hours), limit=str(limit_hours))


# ---------------------------------------------------------------------------
# R-015 — VOID-rate audit: >20% voids in trailing 20 graded
# ---------------------------------------------------------------------------


def _check_r015(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    outcomes = ctx.trailing_graded_outcomes
    if not outcomes:
        return _hit("R-015", allow=True, measured="no_graded_history", limit=None)
    void_rate = Decimal(sum(1 for o in outcomes if o == "VOID")) / Decimal(len(outcomes))
    limit = ctx.dials.void_rate_cap_pct
    ok = void_rate <= limit
    return _hit("R-015", allow=ok, measured=str(void_rate), limit=str(limit))


# ---------------------------------------------------------------------------
# R-016 — promotion metric gates (§9.4) — FLAGGED SEAM, see module docstring
# and PolicyContext.strategy_metrics: real mae.compute_strategy_metrics
# wiring is batch D's job; this rule reads a stubbed summary dict this batch.
# ---------------------------------------------------------------------------


def _check_r016(action: ProposedAction, ctx: PolicyContext) -> RuleHit:
    if ctx.strategy_metrics is None:
        return _insufficient("R-016", "strategy_metrics")
    passes = bool(ctx.strategy_metrics.get("passes_gates", False))
    return _hit("R-016", allow=passes, measured=str(passes), limit="True")


