def applicable(action: ProposedAction) -> tuple[Rule, ...]:
    """Every rule whose `applies_to` includes `action.kind` — pure filter,
    no I/O; the order the pure core (`_evaluate.py`, stub this batch) would
    run them in."""
    return tuple(rule for rule in RULES if action.kind in rule.applies_to)


